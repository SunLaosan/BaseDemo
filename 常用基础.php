
驼峰命名法: 大驼峰 $UserName 小驼峰 $usreName
+++  默认为++ + 可以写成+ ++ 则为+ ++ 否则按默认断开运算
逻辑异或 xor 一真一假为真  注意运算符优先级 && || xor 优先级小于=
if elseif 有真则不再继续判断 同||
1 < $a < 10 无效 必须用逻辑运算符
pow 函数 指数
continue 终止当前循环
冒泡 沉石 快速排序 二分法 插入排序
全局变量 global
方法按引用传递 & 取地址符 传入的是该变量内存地址 方法执行时形参值改变 方法外的变量值随之改变
static 只在第一次调用时初始化 并且保留变量值 所以有记忆功能
